package ute.tkpmgd.EnglishChallenge.exception;

public final class EnglishChallengeException extends RuntimeException {

    /**
	 * 
	 */
	private static final long serialVersionUID = -2713929267961269724L;

	public EnglishChallengeException() {
        super();
    }

    public EnglishChallengeException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public EnglishChallengeException(final String message) {
        super(message);
    }

    public EnglishChallengeException(final Throwable cause) {
        super(cause);
    }

}

